package com.example.doctree.DataModel;

public class SpecialityModel {
    public String specialityIn;
    public int symbol;
    public int symbolBg;
    public int cardBg;

    public SpecialityModel() {
    }

    public String getSpecialityIn() {
        return specialityIn;
    }

    public void setSpecialityIn(String specialityIn) {
        this.specialityIn = specialityIn;
    }

    public int getSymbol() {
        return symbol;
    }

    public void setSymbol(int symbol) {
        this.symbol = symbol;
    }

    public int getSymbolBg() {
        return symbolBg;
    }

    public void setSymbolBg(int symbolBg) {
        this.symbolBg = symbolBg;
    }

    public int getCardBg() {
        return cardBg;
    }

    public void setCardBg(int cardBg) {
        this.cardBg = cardBg;
    }



}
